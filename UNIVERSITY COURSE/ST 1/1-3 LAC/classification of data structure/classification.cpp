// # -----------------Classification of Data Structures in C++-----------------------------
// ## **1.-------------------Introduction**----------------------------
// Data structures are a fundamental part of programming that help in organizing and managing data efficiently. In C++, data structures are broadly classified into **Primitive** and **Non-Primitive** types.

// ## **2. Classification of Data Structures**
// ### **A. Primitive Data Structures**
// These are the basic data types that are directly operated by the machine.
// - **Integer (int)**: Stores whole numbers.
// - **Floating Point (float, double)**: Stores decimal numbers.
// - **Character (char)**: Stores single characters.
// - **Boolean (bool)**: Stores true or false values.
// - **Void**: Represents an empty type.

// ### **B. Non-Primitive Data Structures**
// These are more complex structures derived from primitive types.

// #### **1. Linear Data Structures**
// Elements are arranged in a sequential manner.
// - **Arrays**: Collection of elements of the same type.
// - **Linked Lists**: Elements (nodes) connected using pointers.
// - **Stacks**: Follows LIFO (Last In, First Out) principle.
// - **Queues**: Follows FIFO (First In, First Out) principle.

// #### **2. Non-Linear Data Structures**
// Elements are arranged in a hierarchical or interconnected manner.
// - **Trees**: Hierarchical structure with nodes.
// - **Graphs**: Collection of nodes (vertices) and edges.
// - **Hash Tables**: Stores key-value pairs for efficient lookups.

// #### **3. File Data Structures**
// Used for permanent storage of data.
// - **Text Files**: Store data in human-readable format.
// - **Binary Files**: Store data in a compressed and efficient manner.

// ## **3. Importance of Data Structures in C++**
// - **Efficient data storage and retrieval.**
// - **Optimized memory usage.**
// - **Enhances performance of algorithms.**
// - **Used in software development, databases, and AI.**

